package extControle;

public class For1 {

	public static void main(String[] args) {
		
		//Primeiro For
		System.out.println("primeiro FOR");
		for (int x = 1;x <= 10; x++) {
			System.out.println("X = "+ x);
		}
		//Segundo for
		System.out.println("Segundo FOR");
		int y = 1;
		for (;y < 10;) {
			System.out.println("Y ="+y);
			y++;
		}
	}
}
